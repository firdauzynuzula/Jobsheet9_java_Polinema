public class ArrayRataNilai11 {
    public static void main(String[] args) {
        // Contoh array bertipe integer
        int[] nilaiAkhir = {80, 85, 90, 75, 88, 92, 78, 84, 89, 95};

        System.out.println("Isi array nilaiAkhir:");
        for (int i = 0; i < nilaiAkhir.length; i++) {
            System.out.println("Nilai ke-" + (i + 1) + ": " + nilaiAkhir[i]);
        }
    }
}

